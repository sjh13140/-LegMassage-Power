#include "wdg.h"

/**********************************************************************************************************
**函数名称 ：wdg_init
**函数描述 ：wdg配置
**输    入 ：None
**输    出 ：None
**********************************************************************************************************/
void wdg_init()
{
	OPTION = (OPTION&~OPTION_WDTPSC_MASK)|0<<OPTION_WDTPSC_POS;	//480MS
	AUX2 = (AUX2&~AUX2_WDTE_MASK)|2<<AUX2_WDTE_POS;			        //看门狗在快钟和慢钟模式下使能，空闲/停止/暂停模式下禁止
	CLRWDT = 1;
}